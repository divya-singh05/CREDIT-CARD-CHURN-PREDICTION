import streamlit as st
import numpy as np
import pickle
from tensorflow.keras.models import load_model

# Load model and scaler
model = load_model("model.h5")
scaler = pickle.load(open("scaler.pkl", "rb"))

st.title("Credit Card Churn Prediction (ANN)")

# Inputs
credit_score = st.number_input("Credit Score", 300, 900)
geography = st.selectbox("Geography", ["France", "Germany", "Spain"])
gender = st.selectbox("Gender", ["Male", "Female"])
age = st.number_input("Age", 18, 100)
tenure = st.number_input("Tenure", 0, 10)
balance = st.number_input("Balance")
num_products = st.number_input("Number of Products", 1, 4)
has_card = st.selectbox("Has Credit Card?", ["Yes", "No"])
active_member = st.selectbox("Is Active Member?", ["Yes", "No"])
salary = st.number_input("Estimated Salary")

# One-hot encoding (MUST match training)
geo_germany = 1 if geography == "Germany" else 0
geo_spain = 1 if geography == "Spain" else 0
gender_male = 1 if gender == "Male" else 0

has_card = 1 if has_card == "Yes" else 0
active_member = 1 if active_member == "Yes" else 0

if st.button("Predict Churn"):
    input_data = np.array([[credit_score, age, tenure, balance, num_products,
                            has_card, active_member, salary,
                            geo_germany, geo_spain, gender_male]])

    input_data = scaler.transform(input_data)
    prediction = model.predict(input_data)

    if prediction[0][0] > 0.5:
        st.error("Customer is likely to churn ❌")
    else:
        st.success("Customer will NOT churn ✅")

